// 函数模板偏序规则：
// 如果模板 A 能够处理的所有情况，模板 B 都能处理。而模板 B 能处理的情况，模板 A
// 未必能处理。则模板 A 比 B 更特化。
// 文章的意思大概是：编译器捏造一个类型 U，用类型 U 带入模板
// A，生成一个具体的函数签名。 然后用这个函数签名，去试图匹配模板 B
// 如果能匹配，则说明 A 比 B 特化。
