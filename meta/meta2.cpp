// 主模板
template <typename t, typename u> struct s {
  s(t, u b) { std::print("#1: {}\n", std::is_same_v<decltype(b), float>); }
};

template <typename t> struct s<t, float> {
  s(t, t b) { std::print("#2: {}\n", std::is_same_v<decltype(b), float>); }
};

template <> struct s<int, int> {
  s(int, int b) { std::print("#3: {}\n", std::is_same_v<decltype(b), float>); }
};

