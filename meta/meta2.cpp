// 函数模版既可以重载，又可以全特化
// 函数模板的每一个重载都是主模板
// 实例化过程中，先进行重载决议，然后再特化匹配
// 也就是说，在重载决议阶段，只考虑主模板，不考虑主模板的全特化
// 选择主模板后，才进行特化匹配
// 这样的规则会导致：如果模板特化的位置不同，最终匹配到的模板也有可能不同
// 所以我们不应该使用函数模板全特化，而是使用函数重载

