/*
### 关于类模板的“实参推导”的叫法
在类模板流程中：
```cpp
你的原话：
然后进行实参推导，在实参推导阶段，只依据主模板推导
如果推导发生错误，给出 hard error
```
**微调建议**：在 C++17 之前，常规的类模板实例化（如 `MyClass<int>`）其实**不包含“推导 (Deduction)”的过程**，
因为你已经显式给出了 `<int>`，编译器做的是**“实参匹配与替换检查 (Argument Matching & Checking)”**。
只有在 C++17 引入了 CTAD (Class Template Argument Deduction，如写 `std::vector v = {1, 2, 3};`) 之后，
类模板才有真正的推导过程。

所以，这句话如果改成这样会更无懈可击：
> “然后进行**实参匹配（或 C++17 的 CTAD 推导）**，把你提供的实参套入唯一的主模板参数列表中。
如果实参不满足主模板的基础要求（如参数个数不对、默认参数产生非法类型等），直接给出 hard error。”
*/

