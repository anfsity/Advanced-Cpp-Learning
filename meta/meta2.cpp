// 作者的过程写的很模糊，我们最后来详细总结一下模板实例化的过程
// 总共有五个过程
// 1. 名字查找 name lookup
// 2. 实参推导 argument deduction
// 3. 替换签名 （函数模板独有）substitution into signature
// 4. 重载决议 overload resolution
// 5. 选择特化 specialization selection
// 6. 替换生成实例 instantiation
// 我们分成两条线来处理，一条是函数模板，一条是类/变量模板（接下来统一称做类模板）

// clang-format off
// 函数模板首先进行名字查找，查找到的同名模板纳入一个集合内
// 然后进行实参推导，在实参推导阶段，如果推导发生错误，静默处理
// 实参推导会推导出一批 “代选” 的模板
// 接下来进行替换签名，用推导出来的 T，去替换函数模板的函数列表，返回类型
// 这里会发生 SFINAE
// 经过 SFINAE，代选的模板集合进一步缩小
// 剩下的函数模板和普通函数一起参与重载决议
// 这里还有一个细节，精确匹配的普通函数优先
// 但是，模板实例化的精确匹配优先于普通函数的隐式转化
// 如果只有函数模板，在多个函数模板之间，通过 partial ordering rule 选择出最终的主模板
// 进入选择特化阶段，如果对应的主模板有全特化，对全特化进行抉择
// 最终替换生成实例

// 类模板首先进行名字查找，找到唯一一个主模板
// 然后进行实参推导，在实参推导阶段，只依据主模板推导
// 如果推导发生错误，给出 hard error
// 类模板没有替换签名和重载决议阶段
// 进入选择特化：
// 编译器首先查找出主模板和其所有的偏特化，全特化。构成一个“代选”的模板集合
// 全特化优先，如果有全特化匹配，流程结束，直接替换生成实例
// 如果全特化没有匹配上，尝试匹配偏特化，在尝试偏特化期间，如果产生非法代码，这个偏特化会 SFINAE
// 经过 SFINAE，代选的模板集合进一步缩小
// 如果此时没有偏特化匹配上，选择主模板匹配
// 如果有多个偏特化匹配，通过 partial ordering rule 选择出最终的模板 （类模板被转化为函数模板，归一操作）
// 如果选择不出来，抛出 ambiguous error （歧义错误）
// 最终替换生成实例
